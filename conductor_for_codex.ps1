    return
  }
  $lines = Get-Content $path
  if (-not ($lines -contains $rule)) {
    Add-Content -Path $path -Value $rule
  }
  Write-Host "  Ensured AGENTS.md contains conductor-status rule" -ForegroundColor Green
}

